public class Navigation {
    private String makePayment;
    private String addEmployee;
    private String records;
    private String settings;

    /**
     * @return the makePayment
     */
    public String getMakePayment() {
        return makePayment;
    }

    /**
     * @param makePayment the makePayment to set
     */
    public void setMakePayment(String makePayment) {
        this.makePayment = makePayment;
    }

    /**
     * @return the addEmployee
     */
    public String getAddEmployee() {
        return addEmployee;
    }

    /**
     * @param addEmployee the addEmployee to set
     */
    public void setAddEmployee(String addEmployee) {
        this.addEmployee = addEmployee;
    }

    /**
     * @return the records
     */
    public String getRecords() {
        return records;
    }

    /**
     * @param records the records to set
     */
    public void setRecords(String records) {
        this.records = records;
    }

    /**
     * @return the settings
     */
    public String getSettings() {
        return settings;
    }

    /**
     * @param settings the settings to set
     */
    public void setSettings(String settings) {
        this.settings = settings;
    }   
}
